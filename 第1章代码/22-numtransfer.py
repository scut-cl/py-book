# 数字转化22-numtransfer.py
print("hex(291)=", hex(291))  # 十进制整数转化为十六进制
print("hex(ob11111)=", hex(0b11111))
print("hex(0o234)=", hex(0o234))
print("oct(291)=", oct(291))  # 十进制整数转化为八进制
print("hex(0x234)=", oct(0x234))
print("hex(0b11111)=", oct(0b1111))
print("chr(70)=", chr(70))  # 整数返回Unicode字符
print("chr(49)=", chr(49))
print("chr(0x61)=", chr(0x61))
print("chr(5611)=", chr(5611))
print("chr(1)=", chr(1))
print("chr(0x30)=", chr(0x30))
# 将字符返回Unicode值
print("ord('8')=", ord("8"))
print("ord('T')=", ord("T"))
print("ord('中')=", ord("中"))
print("ord('y')=", ord("y"))
print("ord('β')=", ord("β"))
# 将整数转换为二进制
print("bin(29)=", bin(29))
print("bin(0x11)=", bin(0x11))
print("bin(0o134)=", bin(0o134))
# 将整数转换为十进制
print("int(0x291)=", int(0x291))
print("int(0b11111=", int(0b11111))
print("int(0o234)=", int(0o234))
